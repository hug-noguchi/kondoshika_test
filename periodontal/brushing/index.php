          <div class="l-container">
            <h2>効果的な歯みがきの方法<br class="sp">について</h2>
            <p>
              歯周病治療には、内科的な歯周内科、外科的な歯周外科、歯石除去、ポケット爬(ソウハ)など、多くの方法がありますが、その効果は、ご家庭でのブラシング=ホームケアの状態によって、大きく異なります。<br /><br />
              歯周病の予防においては、もちろんですが、歯周病治療の状況においても、最も重要なのが、日々のブラッシングの方法といえるでしょう。
            </p>
            <h3>最も効果的なブラッシングの方法</h3>
            <div>
              <div class="sec_01_box1">
                <div class="box_left">
                  <p>
                    最も効果的なブラッシングの方法は、歯、歯ぐきの状態が人それぞれ異なるように、個人差があります。
                    <br><br>
                    したがって、その人の口腔内の状態、生活習慣、お仕事などを考慮して、担当の歯科衛生士と相談、アドバイスを受けられるといいでしょう。<br>
                    ここでは、一般的な方法について述べます。
                  </p>
                </div>
                <div class="box_right">
                  <p>↓画像をクリックしていただくと拡大表示になります。</p>
                  <a href="/periodontal/brushing/img/sec_01_image1.jpg" target="_blank">
                    <img src="/periodontal/brushing/img/sec_01_image1.jpg" class="u-image-radius" alt="最も効果的なブラッシングの方法">
                  </a>
                </div>
              </div>
              <p class="underline">さらに、ブラッシングの手法としましては、基本的には以下の2種類の方法があります。</p>

              <div class="sec_01_box2 box_yellow">
                <h4>バス法</h4>
                <div>
                  <div class="box_left">
                    <p>
                      ハブラシの毛先を歯と歯肉のさかい目に向けて45度の角度にあて、軽い力で小きざみに動かします。
                      <br><br>
                      歯ぐきから血が出やすかったりして、歯周病になってしまっている方におすすめします。
                      <br><br>
                      バス法は、より歯ぐきにしっかりと歯フラシがあたるため、とくに弱っている歯ぐきを集中的にケアすることができて効果的です。
                    </p>
                  </div>
                  <div class="box_right">
                    <img src="/periodontal/brushing/img/sec_01_image2.jpg" class="u-image-radius" alt="バス法">
                  </div>
                </div>
              </div>
              <div class="sec_01_box3 box_yellow">
                <h4>スクラビング法</h4>
                <div>
                  <div class="box_left">
                    <p>
                      ハブラシの毛先を歯に直角にあて、軽い力で小きざみに動かします。<br>歯の内側は45度にあてます。
                      <br><br>
                      歯みがきをしていても血が出ない健康な歯ぐきの方は、基本的にはスクラビング法を用いて、ていねいに歯みがきをすれば、おおかたの汚れはとれるでしょう。
                    </p>
                  </div>
                  <div class="box_right">
                    <img src="/periodontal/brushing/img/sec_01_image3.jpg" class="u-image-radius" alt="スクラビング法">
                  </div>
                </div>
              </div>
            </div>
          </div>
        </section>

        <section class="sec_02">
          <h2>おすすめの歯ブラシ<br class="sp">の基本形</h2>
          <div class="l-container">
            <div class="box_white sec_02_box1">
              <p>
                ヘッドが小さめのもので、ネックが細く、毛先はストレート型、毛が多くやわらかいものが最適です。<br>ヘッドが小さいと、口の中で小回りがききます。
              </p>
              <h3>ネックが細い小さめのもの</h3>
              <div>
                <p>
                  ネックが細いと奥歯に届きやすいのです。<br>
                  毛先のストレート型は、スクラビング法、バス法によって効果的に歯ぐきのフラッシングができるのです。<br />
                  また、毛が多いと、やわらかくても、コシが生まれるため、スムーズにプラークをかき出すことができます。
                  <br /><br />
                  かたい歯ブラシは避けてください。<br>エナメル質を削ってしまったり、歯ぐきを痛めます。
                  <br /><br />
                  歯ブラシの交換は2カ月に1.本のペースがおすすめです。
                </p>
              </div>
