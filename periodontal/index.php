          <div class="l-container">
            <h2>歯周病とは</h2>
            <div class="detail_box box_white">
              <p>
                歯周病は感染症です。
                <br /><br />
                歯周病の原因は、口腔消掃状態、<strong>歯ぎしり、食いしばり</strong>などの<strong>ブラキシズム（咬合性外傷）</strong>、<strong>喫煙習慣</strong>、<strong>糖尿病</strong>などの疾患、歯科医院で入れてもらった合わない被せ物、詰め物など多くのものが考えれられますが、やはりベースになるのは、<strong>細菌感染</strong>です。
                <br /><br />
                そして、その細歯をどうしたら、減らせるかが、一番のポイントとなります。
                <br /><br />
                したがって、歯周病治療の第一歩は、お口の中に歯周病菌が多くいるかどうかを、まず、検査することから始めなくてはいけません。
                <br /><br />
                その検査法はいくつかありますが、もっとも簡単で、費用的にも、時間的にも効率的なのが、<strong>位相差顕微鏡</strong>による細菌検査法です。
                <br /><br />
                この、位相差顕微鏡ですが、精度的にも、その場でだけしか見られないような、簡単なものから、記録を録れるものまで、さまざまな種類があります。
                <br /><br />
                当クリニックの位相差顕微鏡は、<strong>国際歯周内科学会</strong>公認のもので、精度はもちろん、パソコンと一体型なので、各ユニットに備え付けられた、大きなスクリーンで、その場で、すぐ見ることができ、もちろん、記録も録ります。
                <br /><br />
                この、<span class="red"><strong>記録を録るということ</strong></span>が重要で、治療、患者さんの生活習慣の改善などによる変化がすぐ、細菌の様子に出てくるのです。<br />
                そして、改善し、いい状態になったものを記録しておくことにより、定期検診などのリコール時などに、比較検討できるのです。
                <br /><br />
                <strong>従来の歯周病治療</strong>では、細菌の状態や、生活習慣的な、個々のそれぞれの状態よりも、ただ、ブラッシングと、歯科医院での、歯石除去、スケーリングなどに終始してきました。<br /><br />
                しかし、実際は個々それぞれ、細菌の状態、生活習慣など、だいぶ違いがあります。
                <br /><br />
                これらを、考慮し、歯周病治療を進めませんと、治療期間がかかったり、外科的処置を勧められたり、いったん状態が良くなっても、すぐにまた、悪くなったりしてしまいます。
                <br /><br />
                <strong>最新の歯周病治療</strong>のページでは、これらの問題点をふまえて、近藤歯科クリニックで行っている、実際の歯周病治療について、書いてみました。<br /><br />
                次に、忘れてはならないのは<strong>"生活習價"</strong>ですが、この中に含まれるものとしましては、 前述のように、<strong>"歯ぎしり、食いしばり"</strong>などの<strong>"ブラキシズム"</strong>、<strong>"喫煙"</strong>、
                <strong>"糖尿病"</strong>、<strong>"女性ホルモン"</strong>、さらに<strong>"口呼吸"</strong>、<strong>"食事の取り方（食生活）"</strong>などもあります。
              </p>
            </div>
          </div>
        </section>

