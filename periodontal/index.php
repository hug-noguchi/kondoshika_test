                $(".p-sp-nav-bg").removeClass("bg-active"); //背景からbg-activeクラスを剥奪
                $("body").removeClass("fixed").css({ top: 0 }); //背景固定を解除
                $(window).scrollTop(scrollPos); //元の位置までスクロール
              }
            });
