            <h2>歯ぎしり、食いしばり<br class="sp">の人へのアドバイス</h2>
            <p class="box_white">
              歯ぎしり、食いしばりは無意識のうちにやっていることがほとんどです。
              <br><br>
              たとえば、本を読んでいる時、パソコンをしている時、車の運転をしている時、家事をしている時など、日常数え切れないほどです。<br>また、睡眠時の、歯ぎしり、食いしばりも多くみられます。
              <br><br>
              では、このような、歯にも、周辺の組織にもダメージを与える"歯ぎしり、食いしばり"に対して、どのように対処していけばいいのでしょうか？
            </p>
