        <section class="sec_03">
          <div class="l-container">
            <h2>日中(睡眠時以外)の状況<br class="sp">での対策</h2>
            <h3>長時間、同じ姿勢を続けないこと</h3>
            <p>
              人、それぞれ違いますが、<span class="red"><strong>長時間、同じ姿勢を続けないことです</strong></span>。
              <br><br>
              本を読んだり、ずっと座って仕事をしている、趣味に熱中する、スポーツを過度にする、長時間の運転をする・・・などです。<br>これらは、力仕事をしているように、筋肉の緊張が高まり、無意識のうちに食いしばっていることが多いです。
              <br><br>
              あと、 最近、多くみられるのが、<span class="red"><strong>パソコンを長時間やることです</strong></span>。<br>インターネットの普及により、情報収集が便利になりましたが、つい、長時間やりすぎてしまうようです。
              <br><br>
              したがって、<span class="red"><strong>何かする時は(人によって違いはありますが)1時間以上、同じ姿勢を続けないことです</strong></span>。<br>座りっぱなしの時などは、ときどき、歩き回って、体をほぐすようにしてください。
              <br><br>
              あと、<span class="red"><strong>食いしばりはいけないと意識し、どんな時に食いしばっているかを、ご自身で確かめてみることです</strong></span>。<br>そして、どんな時食いしばることが多いか確認できれば、意識して、食いしばりを減らしていくことが可能です。
              <br><br>
              さらに、長時間の運転やスポーツ時、仕事など、しなくてはならない時はマウスピースをつけるのも効果的です。<br>ただ、マウスピースはあまり習慣的に長期間つけると、顎関節などによくないといわれています。
            </p>
          </div>
        </section>

