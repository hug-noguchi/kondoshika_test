            <h2>最新の歯周病治療</h2>
              <div class="problem_box">
              <h3>現在の歯周病治療の問題点</h3>
              <p>
                健康な状態の口腔内の微生物叢の研究がない。<br />
                口の中はどんなに健康な人でも無菌状態ということはありません。<br />
                しかし、これくらいの細菌の数、種類なら安全という目安は必要だと考えられます。
                <br /><br />
                歯周病菌を減らさずに、いきなり歯石を取ったり、歯茎の掃除をしている。<br />
                歯周病菌は心臓病や糖尿病など、<strong>内科的な病気と密接な関係があり</strong>、細菌を減らさずに、出血させて、<br />
                歯や歯茎の掃除をする事は危険である。<br />
                <span class="mark_pink">これは消毒しないで、外科処置をするようなものです。</span>
                <br /><br />
                <strong>感染症であるのに</strong>、どこから感染するのか研究がない。<br />
                <span class="mark_pink"><strong>歯周病は感染症です</strong>。</span><br />
                他の人から感染するのです。<br />
              </p>
              <p><strong>歯周病口とカンジタ（真菌）との相互作用</strong>についての研究が不足している。</p>
            </div>
            <div class="problem_box_02">
              <div>
                <h3>最新の歯周病治療について</h3>
                <h4 class="red underline">○歯周病菌とカンジダ菌との相互作用について</h4>
                <ul class="box_yellow">
                  <li>
                    1. まず、<strong>カンジダ菌が歯茎にくつつき、根を下ろし炎症を起こします</strong>。<br />
                    歯周病菌は歯ブラシで取れますが、<strong>カンジダはしっかり根を下ろしてしまいます</strong>。
                  </li>
                  <li>2. 炎症によって歯茎が腫れ、歯と歯茎との間にポケットができ、歯周病菌がたまり、さらに<strong>炎症がひどくなり骨が溶けていきます</strong>。</li>
                  <li>3. ポケットはさらに深くなり、さらに多くの菌がたまり、<strong>骨が溶けて、歯がグラグラになって抜けてしまいます</strong>。</li>
                </ul>
              </div>
              <div>
                <h4 class="red underline">○最新の歯周病治療（歯周内科）の流れ</h4>
                <ul class="box_yellow">
                  <li>
                    1. 位相差顕微鏡などで、<strong>現在の細菌叢</strong>を調べます。<br />
                    その場で<strong>当日2~3分で検査</strong>できます。（検査 料はいりません）
                  </li>
                  <li>2. カンジダ菌が多くみられる場合、<span class="underline"><strong>カンジダ菌に有効な、歯みがき剤（液体またはジェルタイプ）で歯だけでなく歯茎にも、よく、つけてもらいます。</strong></span></li>
                  <li>3. 歯周病菌（運動性棒菌、 スピローヘタの種）が多く、 歯茎も腫れていて重症の場合は抗生剤を出すことがあります。</li>
                  <li>4. カンジダ菌は歯茎に根を張っているので、普通こすってもとれませんが、この歯みがき剤をつけ て磨くと効果があり、それを10日～2W後にまた、位相差顕微鏡により、効果を確認します。</li>
                  <li>5. 歯周病菌が多い重篤な場合も抗生剤投薬後、顕微鏡で効果を確認します。</li>
                  <li>
                    6. 除菌効果確認後、歯石を取ったり、歯茎の掃除のどの通常の歯周病治療を行います。<br />
                    歯周病菌は細菌性心内膜炎、脳梗塞、心筋梗塞などの原因にもなる可能性があり、除菌せずに、出血を伴う処置または強い力でのブラッシングは危険です。
                  </li>
                </ul>
              </div>
            </div>
