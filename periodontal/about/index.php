            <h2>使用する薬剤について</h2>
            <div class="box_white">
              <p>
                重症の場合、悪い細菌の種類が多く、しかも歯ぐきも腫れていて、膿をだすくらいの場合には<strong>ジスロマック</strong>という抗生物質をだします。
                <br /><br />
                <span class="red">ジスロマックは歯周病菌によく効く理想的な抗生物質です。</span>
                <br /><br />
                「ジスロマックは何故効くのか」(歯周病は薬で治る・生田図南著・天の草者発行) ジスロマックは抗生物質の理想型といえます。<br />
                治療薬剤としては1991年イギリスにおいて最初に採用されました。<br />
                日本においては2000年6月より保険適用になりました。
                <br /><br />
                ジスロマックは抗生物質として薬剤が持つべき最適な用件をほぼ100%満たしています。<br />
                つまり、最小使用量で最高の効果を長時間維持できるという特性です。<br />
                さらに最近の研究でジスロマックは耐性菌が非常に出にくい薬であることが証明されました。
