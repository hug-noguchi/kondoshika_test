          <div class="l-container">
            <h2>従来の一般的な<br class="sp" />歯周病治療の流れ</h2>
            <div class="flow_box box_white">
              <h3>一般的な歯周病治療の流れ</h3>
              <div class="flow_list">
                <ul>
                  <li>① 基本検査</li>
                  <li>② ブラッシング指導</li>
                  <li>③ スケーリング（歯石をとったり、歯ぐきの掃除をします）</li>
                  <li class="pc">④ 再検査　　　　　　　　　　　　　　　　<strong>⇒治癒</strong></li>
                  <li class="sp">④ 再検査　　　　　　　　<strong>⇒治癒</strong></li>
                  <li>⑤ ルートプレーニング、歯周ポケット掻爬（さらに歯ぐきの深いところの掃除をします）</li>
                  <li class="pc">⑥ 再々検査、精密検査　　　　　　　　　　<strong>⇒治癒</strong></li>
                  <li class="sp">⑥ 再々検査、精密検査　　<strong>⇒治癒</strong></li>
                  <li>⑦ 歯周外科手術（外科的に歯ぐきの悪いところ、再生不可のところを切除します）</li>
                  <li class="pc">⑧ 再精密検査　　　　　　　　　　　　　　<strong>⇒治癒</strong></li>
                  <li class="sp">⑧ 再精密検査　　　　　　<strong>⇒治癒</strong></li>
                  <li>⑨ メンテナンス（半年～1年後）</li>
                  <li>（検査の内容は、ポケット測定、動揺度、プラークスコアなどで、精密検査はさらにこれらの細かいものです。）</li>
                </ul>
              </div>
              <p>
                以上のような流れです。
                <br /><br />
                <strong>これは私が学生時代教えられた時期から(30年間)ずっと変わってません。</strong>
                <br /><br />
                <span class="space">治療期間も検査、再検査、再々検査、精密検査など、何度も検査を行い、しかも、スケーリング、ルートプレーニングにしても、4～6回に分けて行います。</span>
                <br /><br />
                このため、<strong>歯周病治療は時間がかかるのです。</strong>
                <br /><br />
                さらに、時間、回数をかけても必ず治るとは限りません。
                <br /><br />
                歯周病とは、歯を支えている骨(歯槽骨)が溶ける病気ですが、重症な場合は治りません。
                <br /><br />
                再生療法(エムドゲイン、GTR法)もありますが、適応にはいくつかの条件があります。
                <br /><br />
                それに、1年くらいかけて歯科に通っていったん治癒しても、ちよつと油断して、歯磨きをさぼったりすると、また、すぐに、もとの悪い状態に後戻りしてしまいます。
                <br /><br />
                あと、歯の本数が少なくなればなるほど、<strong>咬合性外傷</strong>の影響もでてきて、加速度的に歯が悪くなって、グラグラになり、抜けていきます。
                <br /><br />
                <strong>歯周病の治療はこのように、時間、回数、手間がかかり、おまけに、治りにくく、しかも、前述のように全身にもかかわる、やっかいなものです。</strong>
                <br /><br />
                <span class="red"><strong>歯周内科治療</strong>は患者さんに<strong>位相差顕微鏡</strong>を使用し<strong>実際に細国の状態を確認</strong>してもらいながら、場合によっては、抗生剤を使用し、<strong>効率よく、短期間で</strong>、患者さんにも<strong>目で確認</strong>してもらう分、<strong>再発が少ない理想的な歯周病の治療法</strong>です。
                <br /><br />
                <strong>治療期間も今まで1年以上かかっていたものが、半年以内に終了するようになりました。</strong></span>
                <br /><br />
                もちろん、保険治療の流れに沿って行いますので、費用も歯磨き剤(1本1700円)以外にはかかりませんし、基本検査、精密検査、口腔写真撮影も通常どおり行っております。
                <br /><br />
                抗生剤を処方する場合は、細菌数が多く、しかも、急性症状などで、歯ぐきが腫れていて、切開し、ウミを出さなければいけない状態の時です。
                <br /><br />
                あと、歯周病再発予防にはメンテナンスが必須ですが、位相差顕微鏡で細菌を見られると、ほとんどの方はリコールに来られます。<br />
                したがって、メンテナンスも行え、再発が予防できています。
              </p>
            </div>
          </div>
