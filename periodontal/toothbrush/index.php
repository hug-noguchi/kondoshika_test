          <div class="l-container">
            <h2>電動歯ブラシについて</h2>
            <p>
              ブラシ自体が高速で運動するため、歯ブラシを歯面に当てるだけで汚れ(プラーク)を除去することができます。
              <br><br>
              身体が不自由で、手用歯ブラシを上手に動かすことができない方や、毛先を当てる場所と方向を自分自身で認識できる、ブラッシングが上手な方におすすめです。
              <br><br>
              振動のタイプによって以下の3つに分類されます。
            </p>
          </div>
        </section>

        <section class="sec_02">
          <div class="l-container">
            <h2>電動歯ブラシの種類</h2>
            <h3>振動による3つのタイプ</h3>
            <ul>
              <li class="box_white">
                <span class="list_title">1.回転運動型</span>
                <br><br>
                回転運動するブラシの外側の清掃性が高く、中央部が低いという特徴があります。<br>そのため、ブラシの最も外側の毛先部分をう蝕や歯周病のなりやすい歯と歯の間や歯と歯ぐきの境目あたりに沿って少しずつずらしながら使用します。
                <br><br>
                また、ブラシが円形であるため、歯の湾曲に沿わせやすいのが特徴です。
              </li>
              <li class="box_white">
                <span class="list_title">2.前後(左右)振動型</span>
                <br><br>
                「前後振動型」の電動ブラシは、前後に歯ブラシを動かすと毛の運動方向と重なるため、同じところばかりが擦られ磨き残しが生じます。
                <br><br>
                そのため、前後振動型のものでは、歯面に当てる際に上下にすこしずらしながら磨くといいでしよう。
                <br><br>
                同様に左右振動型のものは、前後に少しずつ、ずらしながら歯面に当てるようにします。
              </li>
              <li class="box_white">
                <span class="list_title">3.微振動型</span>
                <br><br>
                微振動のものは、振動の幅が極めて小さいために、歯面にやさしく当たる反面、歯みがき粉の粘度が高いとブラシの毛先の動きが阻害され、歯面の清掃効率が低下することがあ ります。
              </li>
            </ul>
          </div>
        </section>

        <section class="sec_03">
          <div class="l-container">
            <h2>音波ブラシについて</h2>
            <p>
              音波ブラシは、毛先の振動が電動歯ブラシのそれを超えて音波の領域になり、その微振動により、プラーク(歯垢)を取り除くものです。
              <br><br>
              音波ブラシの機種によっては、毎分31000回、超音波振動するとされています。
              <br><br>
              また、超音速の振動に加えて、毛先の振幅による液体流動力により、直接毛先をプラークに当てなくても、毛先から離れた部分のプラークも除去できる機種もあります。
            </p>
          </div>
        </section>

        <section class="sec_04">
          <div class="l-container">
            <h2>音波歯ブラシの利点<br class="sp">と使い方</h2>
            <p class="box_white">
              電動歯ブラシと同様に、手をこまめに動かす必要がないことが利点ですが、音波を利用して広範囲のプラークを除去できる可能性をもっています。
              <br><br>
              使用法は、歯へは軽く接触するような気持でゆっくりと当てます。<br>そして電源を入れてみて、刺激が強すぎないように当たり方を調整します。
              <br><br>
              すべての歯の面に順序よく毛先の角度など考えながら磨いていくところは他の歯ブラシと同じです。<br>効果的な毛先の当て方、順番を理解しておく必要があります。
              <br><br>
              この音波歯ブラシは<span class="red"><strong>歯肉のマッサージ効果</strong></span
              >や、歯ブラシの毛先が直接当たらない部分での<span class="red"><strong>プラーク除去効果</strong></span>があるともいわれています。
              <br><br>
              しかし、毛先の接触による除去が一番です。
            </p>
          </div>
        </section>

        <section class="sec_05">
          <div class="l-container">
            <h2>音波歯ブラシの欠点</h2>
            <p>
              電動歯ブラシと同じく、やはり毛先が広がったものはプラークを取る効率が落ちてしまいますので交換すべきですが、ヘッドの交換コストが普通の歯ブラシより高くなります。
              <br><br>
              また、重さがあり、グリップは太いこと、電気で動くので充電が必要なことも欠点といえます。
              <br><br>
              以前と比べ、ヘッドの大きさはかなり小さくなっていますが、口の中では少し大きいと感じる場合もあります。
              <br><br>
              これらは、電気の力で動かす歯ブラシに共通の欠点といえます。
            </p>
          </div>
        </section>

