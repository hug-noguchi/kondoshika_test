                &nbsp;</p>
              <p>非行少年には、身体的虚弱性や病弱性が頻繁にみられることが、研究者によって、指摘され、犯罪を犯す心理学的原因のうち最も一般的かつ重大なものは、普通知的障害であるといわれています。

                &nbsp;</p>
              <p><b>
                  <font color="#69a84f">○プライス博士の食生活の調査

                    &nbsp;</font>
                </b></p>
              <p>その地域における孤立化集団と近代食がはいってきた近代化集団をプライス博士は比較検討しました。<br />（スイス、アラスカ、北アメリカ、メラネシア、アフリカ、オーストラリア、ニュージーランド、ペルーなど）
                &nbsp;</p>
              <p>たとえば、峡谷の海から離れた地方では、氷河と接している牧草地の草を食べた牛のバターを貴重な栄養源としました。

                &nbsp;</p>
              <p>
                島では、魚介類を主食にし、極寒の地方では海で生きている動物の内臓、魚卵など、アフリカでは家畜（肉、ミルク、血）や時には昆虫や蟻など、植物性の食物が手に入らないところでは、動物性のもので、その土地でとれるものを食料にしていました。

                &nbsp;</p>
              <p>ところが、<font color="#fe0000">
                  近代化に伴い、精製された小麦粉、甘い果物、ジャム、マーマレード、砂糖、シロップなどが入手できるようになると、重症の虫歯だけでなく、以下の退化傾向がすぐ子どもの世代から出現しはじめました。



                  &nbsp;</font>
              </p>
              <p></p>
              <ul>
                <li>歯並びが悪くなる。&nbsp;</li>
