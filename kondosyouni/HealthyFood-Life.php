              </p>
              <p>
                <font color="#fe0000">基本的に彼らは驚くほど親切で献身的であり、“全能の、万物に棲み給わうある力に対して、心から崇敬の念をもっている”と感じました。&nbsp;
                </font>
              </p>
              <p>
                <font color="#fe0000">彼らは、大自然の法則に従い、その精神性も高く、たえず敬虔な心と深い信仰をもって、宗教人として、神とともに生きています。</font>&nbsp;
              </p>
              <p>したがって、先住民の文化に対して強い賞讃の念をもちながら、私たちは自分たちの文化を伝え、先住民が受け継いできた知識から得られる教訓を受け取ることが重要なのです。&nbsp;
              </p>
              <p>歯列不正は、親子間一代でも、食生活の変化により、生じる“退化傾向”というのは驚きでした。&nbsp;
              </p>
              <p>学校検診などで、歯並びがいい人は滅多にいませんが、実際、一、ニ世代前では、歯並びがいいのが当たり前だったのでしょう。&nbsp;
              </p>
              <p>また、“人類は退化傾向にある！！”こともショックでした。&nbsp;
              </p>
