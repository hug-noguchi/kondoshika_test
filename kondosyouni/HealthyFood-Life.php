              <p><br /></p>
              <p><b>
                  <font color="#69a84f">○なぜ先住民から学ぶのか

                    &nbsp;</font>
                </b></p>
              <p>
                先住民の多くは、何千年もの間、同じ土地にあって健康を維持していますが、アメリカ人は２、３世紀の間に、しかもある地方などは２０年から３０年しか経っていないのに急速に健康が衰えてきています。（１９２０年ころ）退化が起こっている地域では、動物にもそれが見られます。

                &nbsp;</p>
              <p>
                衰退の一途をたどっている人間は自分自身を治すことはできませんが、今や明らかとなった先住民の知恵を活用することによって、進行しつつあるこの衰退に次の世代で歯止めをかけたり、その世代の置かれている状態を大きく改善したりすることは可能です。

                &nbsp;</p>
