                  <b>子どもの飲み物は、水・麦茶・ほうじ茶</b><br />現代の子どもたちの食生活で最も大切なことは、飲み物の選択です。<br />成長期の子どもは、代謝が激しい（水分の入れ替えが大きい）ため、水分の欲求が多いのが特徴です。<br />
                  <font color="#fe0000">
                    &nbsp;“果汁１００％ジュース”や“イオン飲料”、“清涼飲料水”のように熱量の高い飲み物で熱量を摂ってしまい、きちんと食事の出来なくなっているお子さんがたくさんいます。</font><br />
                  <font color="#3c78d8">飲み物は水分を補給するものであって、熱量（カロリー）を摂るものではありません。</font><br />&nbsp; &nbsp;<br />
                </li>
                <li><b>朝ごはんをしっかり食べさせる</b><br />子どもたちにとって、大切なことは、少なくとも１日に２回以上は「ご飯」を食べさせることです。
                  <br />子どもの場合、幼稚園、学校の給食で、パンや麺類になる可能性がありますので、ご飯を１日２回以上にするには、朝食をご飯にする必要があります。<br />味噌汁もわざわざ作らなくても、前の晩のものを温めなおしたもので十分です。
                  <br />副食も、漬物や納豆、ふりかけ、佃煮、焼のりなどの常備食でいいです。 <br />&nbsp;&nbsp;<br /></li>
                <li><b>子どものおやつは食事</b><br />
                  <font color="#fe0000">子どものおやつは「４回目の食事」です。</font>
                  <br />したがって、おにぎりやのり巻きなどがベストです。<br />あるいは、うどん、そば、さつまいも、トウモロコシ、せんべいなどがお勧めです。（ただし、大人はお菓子でもいいです）<br />
                  <font color="#3c78d8">砂糖の入ったお菓子や油脂類の多いお菓子は、極力控えてください。</font><br />
                  <font color="#fe0000">そのなかでも、スナック菓子と清涼飲料水、スポーツ飲料、乳酸菌飲料は買わないようにしてください。 &nbsp;</font>&nbsp;<br /><br />
                </li>
                <li>
                  <b>カタカナ主食は日曜日に</b><br />パン・ラーメン・シリアル・パスタ・ピザ・ハンバーガー・焼きそば・お好み焼きなど「油型」の主食は、週に１、２回までにするようにしましょう。<br />ご飯以外の主食としては、もち・うどん・そば・そうめんなどの「ひらがな」主食をお勧めします。
                  &nbsp;<br />&nbsp;<br /></li>
                <li><b>副食の基本は野菜、海草類</b><br />副食は季節の野菜、海草類、いも類で、具体的には、サラダや炒め物よりも、煮物、和え物、おひたしなど油の少ないものがいいです。
                  &nbsp;&nbsp;<br /><br /></li>
                <li><b>動物性食品は魚介類を中心とする</b><br />魚介類は高級魚（養殖しているため）よりも、一般的な安い魚のほうが安全性は高いです。 &nbsp;&nbsp;<br /><br /></li>
                <li>
                  <b>未精製の米を常食にする</b><br />一番のお勧めは５分づき米です。栄養素は十分に残っているのもかかわらず、比較的白いため食べやすいためです。<br />玄米は食べやすさという点で、家族全員で、続けていくということが難しくなる場合が多いからです。
                  &nbsp;&nbsp;<br /><br /></li>
                <li><b>食品の安全性にも配慮する

                  </b><br />神経質にならない程度に、食品の安全性に配慮することもいいでしょう。
                  <br />ただし、食生活全体を見直してから考えるのが正しい順番です。
                  <br />毎朝、パンを食べながら食品添加物を考えるとなると、大変な労力とお金がかかります。<br />ご飯、野菜（海草）、豆類、魚介類中心の生活をしていれば、その時点で食品添加物などの心配は少なくなります。
