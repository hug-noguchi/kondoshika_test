                <font color="#69a84f">&nbsp;○伝統食と近代食の特徴</font>
              </b>
              <p></p>
              <p>ここは、「食生活と身体の退化の本文を一部、引用します。」&nbsp;
              </p>
              <p>「肉体でも、精神でも、また道徳面でも退化を防止するうえで、先住民の方が近代化された集団よりも優秀なのはなぜだろうか。&nbsp;
              </p>
              <p>
                <font color="#3c78d8">その唯一の答えは、先住民が大自然の法則に従った生活をしていることである。</font>&nbsp;
              </p>
              <p>先住民の食事は多種多様であるが、そこにはある共通の特徴がみられる。&nbsp;
              </p>
              <p>病気に対する免疫が高く、奇形が見られないということです。&nbsp;
              </p>
              <p>
                <font color="#3c78d8">その地域で採られている食物は、身体の形成、回復物質を適切に供給してくれる食物なのである。&nbsp;
                </font>
              </p>
              <p>相対的に低カロリーの食品を先住民は食べているので、身体が要求する熱とエネルギーを補うためにはそうした食物を大量に食べなければ、ならなくなる。&nbsp;
              </p>
              <p>
                先住民たちは、数は少ないが成分には富んでいるというような食物を、時には非常な努力を払って確保してきたのであるこういった希少な食品には、ヨード、銅、マンガンなどのミネラル類やある種のビタミンのような、たとえ少量しか必要でなくとも、非常に重要な成分が含まれている。&nbsp;
              </p>
              <p>ただ、近代商業は保存がきく食料だけを未開社会に送り込んでいる点が違っているわけである。共通する食品としては、主として精白小麦粉、砂糖、精白米、植物油のほか缶詰製品が挙げられる。&nbsp;
              </p>
              <p>虫歯に対する高い免疫性をもち、その他の身体の退化過程とも無縁な未開集団の食事には最低必要量の４倍以上の栄養が含まれていた。&nbsp;
              </p>
              <p>そして反対に、ほとんどが商業用の精白小麦製品、ジャム、精白米などの食品群にとって代わった所では、どこもこの最低量にも達していなかった。&nbsp;
              </p>
              <p>近代の食品業は、空腹を満たすエネルギー要素を残してはいるものの、自然の食品がもっている、ある種の身体形成物質のほとんどを故意に取り去ってしまっている。&nbsp;
              </p>
              <p>例えば、精白粉を作る時には、リン分やカルシウム分の８０％、すなわち５分の４はたいがい失くしてしまうし、それとともに、胚芽がもっているビタミンやミネラルも取り除かれてしまうのである。」&nbsp;</p>
