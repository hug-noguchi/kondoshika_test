                舌の位置と口の周りの筋肉の状態を変えていく（受け口は舌の位置と口の周りの筋肉の動きが原因）装置（３５００円）をつけて治療していきます。
                <br />子どもの前歯が永久歯に生え換わる時期（６～７歳）にも、前歯を前に出す床矯正装置を使用することもあります。

